package Integración.query;

public class VIPResultado {
	private Object res1;
	private Object res2Valor;
	
	public VIPResultado() {}
	
	public VIPResultado(Object res1, Object res2) {
		this.res1 = res1;
		this.res2Valor = res2;
	}
	
	public Object getRes1() {
		return res1;
	}
	public void setRes1(Object res1) {
		this.res1 = res1;
	}
	public Object getRes2Valor() {
		return res2Valor;
	}
	public void setRes2Valor(Object res2Valor) {
		this.res2Valor = res2Valor;
	}
	
	
}
