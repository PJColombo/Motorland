    private void altaBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_altaBActionPerformed
    	if(dniTF.getText().isEmpty() || domicilioTF.getText().isEmpty() || 
    			nombreTF.getText().isEmpty() || tarjetaTF.getText().isEmpty() ){
   		 	JOptionPane.showMessageDialog(this,"Faltan campos por rellenar", "Alta cliente", JOptionPane.ERROR_MESSAGE);
	   	}
	   	else{
	   		try {
		   		long tarjeta = Long.parseLong(tarjetaTF.getText());
		   		String nombre = nombreTF.getText();
		   		String domicilio = domicilioTF.getText();
		   		String dni = dniTF.getText();
		   		
		   		TCliente t = new TCliente(-1, nombre, dni, domicilio, tarjeta, true);
		   		
		   		Controller.getInstance().run(ListaComandos.NEGOCIOALTACLIENTE, t);
	   		}
	   		catch(NumberFormatException e) {
	   			JOptionPane.showMessageDialog(this,"Formato del campo incorrecto.", "Alta cliente", JOptionPane.ERROR_MESSAGE);
	   		}
	   		
   		}
    }//GEN-LAST:event_altaBActionPerformed

    private void bajaBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bajaBActionPerformed
    	if(idTF.getText().isEmpty()){
    		JOptionPane.showMessageDialog(this,"El campo ID esta vacío.", "Baja cliente", JOptionPane.ERROR_MESSAGE);
    	}

    	else{
	   		int id =Integer.parseInt(idTF.getText());
	   		try {
		    	Controller.getInstance().run(ListaComandos.NEGOCIOBAJACLIENTE, id);
		    	
	   		}
	   		catch(NumberFormatException e) {
	   			JOptionPane.showMessageDialog(this,"Formato del campo ID incorrecto. Introduzca un numero entero positivo.", "Baja cliente", JOptionPane.ERROR_MESSAGE);
	   		}
	   		
	    	limpiarComponentes();

    	}
    }//GEN-LAST:event_bajaBActionPerformed

    private void modificaBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificaBActionPerformed
    	if(dniTF.getText().isEmpty() && domicilioTF.getText().isEmpty() && nombreTF.getText().isEmpty() && 
    			tarjetaTF.getText().isEmpty()){
   		 	JOptionPane.showMessageDialog(this,"Campos vacíos. Rellene aquellos campos que desea modificar.", "Modificar cliente", 
   		 			JOptionPane.ERROR_MESSAGE);
	   	}

    	else{
    		if(idTF.getText().isEmpty()){
    			JOptionPane.showMessageDialog(this,"Introduzca el ID del cliente que quiere modificar.", "Modificar cliente", 
       		 			JOptionPane.ERROR_MESSAGE);
    		}
    		else {
    			try {
	    			int id = Integer.parseInt(idTF.getText());
	    			String nombre = nombreTF.getText();
	    			String dni = dniTF.getText();
	    			String domicilio = domicilioTF.getText();
	    			long tarjeta = Long.parseLong(tarjetaTF.getText());
	    			
			    	TCliente t = new TCliente(id, nombre, dni, domicilio, tarjeta, true);
			   		
			    	Controller.getInstance().run(ListaComandos.NEGOCIOMODIFICARCLIENTE, t);
			    	
    			}catch(NumberFormatException e) {
    				JOptionPane.showMessageDialog(this,"El formato de alguno de los campos es incorrecto.", "Modificar cliente", 
           		 			JOptionPane.ERROR_MESSAGE);
    			}
		    	
    		}
    	}
    }//GEN-LAST:event_modificaBActionPerformed

    private void consultaBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultaBActionPerformed
    	if(idTF.getText().isEmpty()){
    		JOptionPane.showMessageDialog(this,"Introduzca el ID del cliente que quiere consultar", "Consulta cliente",
    				JOptionPane.ERROR_MESSAGE);
    	}
  
    	else{
    		try {
		    	int id = Integer.parseInt(idTF.getText());
		    	if(id <= 0)
		    		throw new NumberFormatException();
		    	
		    	Controller.getInstance().run(ListaComandos.NEGOCIOCONSULTARCLIENTE, id);
		    	
    		}catch(NumberFormatException e) {
    			JOptionPane.showMessageDialog(this,"Formato del campo ID incorrecto. Introduzca un numero entero positivo.", "Consulta cliente",
        				JOptionPane.ERROR_MESSAGE);
    		}
	    	
    	}
    }//GEN-LAST:event_consultaBActionPerformed

