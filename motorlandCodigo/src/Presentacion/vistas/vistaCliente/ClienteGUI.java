    private void eliminaBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminaBActionPerformed
        int id;
        
        try {
        	if(idTF.getText().isEmpty())
        		JOptionPane.showMessageDialog(this,"Campo ID vacío.", "Elimina cliente",
        				JOptionPane.ERROR_MESSAGE);
        	else {
        		id = Integer.parseInt(idTF.getText());
        		
        		if(id <= 0)
        			throw new NumberFormatException(); 
        		
        		Controller.getInstance().run(ListaComandos.NEGOCIO_ELIMINA_CLIENTE, id);
        	}
        }
        catch(NumberFormatException e) {
        	JOptionPane.showMessageDialog(this,"Formato del campo ID incorrecto. Introduzca un numero entero positivo.", "Elimina cliente",
    				JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_eliminaBActionPerformed

